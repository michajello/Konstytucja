package agh.cs.konstytucja;

public class RangeOfArticleInChapters {
	private int beginningOfRange ;
	private int endOfRange;
	
	public RangeOfArticleInChapters(int beginningOfRange, int endOfRange) {
		this.beginningOfRange = beginningOfRange;
		this.endOfRange = endOfRange;
	}

	public int getBeginningOfRange() {
		return beginningOfRange;
	}

	public int getEndOfRange() {
		return endOfRange;
	}
	
	
	
}
